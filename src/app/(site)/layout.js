import {  Montserrat } from "next/font/google";
import "@fortawesome/fontawesome-free/css/all.min.css";
import "../globals.css";
import Navbar from "@/Components/Navbar/Navbar";
import Footer from "@/Components/Footer/Footer";

const montserrat =Montserrat({
  subsets: ["latin"],
  weight: ["300", "400", "500", "700"],
  variable: "--font-montserrat",
});



export const metadata = {
  title: "Racing Line Portfolio",
  description: "Generated by create next app",
};


async function getSiteSettings() {
  const res = await fetch(`${process.env.NEXT_PUBLIC_SITE_URL}/api/globals/site-settings?depth=1`, {
    cache: "no-store",
  });

  if (!res.ok) return null;
  return res.json();
}

async function getContactInfo() {
  const res = await fetch(`${process.env.NEXT_PUBLIC_SITE_URL}/api/globals/contact-info?depth=1`, {
    cache: "no-store",
  });

  if (!res.ok) return null;
  return res.json();
}

export default async function SiteLayout({ children }) {
  const [data, contactInfo] = await Promise.all([
    getSiteSettings(),
    getContactInfo(),
  ]);


  return (
    <html lang="en">
      <body className={`${montserrat.variable} antialiased`}>
        <div className="container mx-auto px-5 sm:px-10 lg:px-14">
          <Navbar navItems={data ? data.navItems : []} logo={data?.logo} />
          {children}
        </div>
        <Footer
          footerDesc={data?.footerDescription}
          navItems={data?.navItems ?? []}
          offices={contactInfo?.offices ?? []}
          socialLinks={contactInfo?.socialLinks ?? {}}
        />
      </body>
    </html>
  );
}
