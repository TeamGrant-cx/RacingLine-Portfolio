import { Montserrat, Playfair_Display } from "next/font/google";
import "@fortawesome/fontawesome-free/css/all.min.css";
import "../globals.css";
import Navbar from "@/Components/Navbar/Navbar";
import Footer from "@/Components/Footer/Footer";

const montserrat = Montserrat({
  subsets: ["latin"],
  weight: ["300", "400", "500", "700"],
  variable: "--font-montserrat",
});

const playfair = Playfair_Display({
  subsets: ["latin"],
  weight: ["400", "500", "600", "700"],
  variable: "--font-playfair",
});

export const metadata = {
  title: "Racing Line Portfolio",
  description: "Generated by create next app",
};

async function getSiteSettings() {
  const res = await fetch(`${process.env.NEXT_PUBLIC_SITE_URL}/api/globals/site-settings?depth=1`, {
    cache: "no-store",
  });
  if (!res.ok) return null;
  return res.json();
}

async function getContactInfo() {
  const res = await fetch(`${process.env.NEXT_PUBLIC_SITE_URL}/api/globals/contact-info?depth=1`, {
    cache: "no-store",
  });
  if (!res.ok) return null;
  return res.json();
}

